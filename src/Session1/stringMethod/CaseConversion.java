package Session1.stringMethod;

public class CaseConversion {
    public static void main(String args[]) {
        String string1 = "HELLO WORLD";
        String string2 = "WELCOME TO QAcademy";
        String lowerString1 = string1.toLowerCase();
        String lowerString2 = string2.toLowerCase();
        System.out.println(lowerString1);
        System.out.println(lowerString2);
        System.out.println(lowerString1.toUpperCase());
        System.out.println(lowerString2.toUpperCase());
    }
}